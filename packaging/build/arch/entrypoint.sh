#!/bin/sh
# entrypoint.sh -- honcho-inspector-ui Arch package builder.
#
# Same flow as the debian/rocky/suse/alpine entrypoints, with two
# material differences:
#
#   1. NO fpm. makepkg is the build tool. We generate a PKGBUILD
#      and a .INSTALL (pacman-style install script) on the fly and
#      let makepkg produce the .pkg.tar.zst. The PKGBUILD lives
#      next to the staged source; makepkg's $srcdir defaults to
#      the PKGBUILD's directory when source=() is empty, which
#      lets our package() function reach into $srcdir directly.
#
#   2. makepkg REFUSES to run as root (EUID 0). We drop privileges
#      to a non-root `build` user via runuser (PAM-free, lives in
#      coreutils), and wrap makepkg in fakeroot so the package()'s
#      `install -o root -g www-data` calls record root ownership
#      in the .pkg.tar.zst metadata even though the build user
#      never actually has CAP_CHOWN on the host.
#
# Why we do not strip the packaging/ tree before staging:
#   makepkg's $srcdir is the PKGBUILD's directory. We stage /src
#   into /tmp/stage, drop a PKGBUILD and .INSTALL in there, and
#   let makepkg do its thing. Stripping packaging/ from the staged
#   tree would also strip packaging/container/entrypoint.sh which
#   the package() function moves into /usr/local/bin/. We instead
#   let the packaging tree land at /usr/local/share/.../
#   packaging/ inside the package (harmless -- it's just a few KB
#   of build scripts) and only move the bits we want at canonical
#   paths.
#
# Why we strip the noise but keep .pkg-scripts/ around:
#   .pkg-scripts/{postinst,prerm,postrm,changelog} are debian-flavoured
#   scripts that pacman does not use (pacman reads .INSTALL, not
#   .pkg-scripts/postinst). They're kept in /tmp/stage only so the
#   package() function can install the changelog from
#   /tmp/stage/.pkg-scripts/changelog; the package() function then
#   removes .pkg-scripts/ from $pkgdir before makepkg tars it up.

set -eu

PROJECT="honcho-inspector-ui"
# Arch pkgver forbids `-`; use `.` instead. 0.1.0.SNAPSHOT is the
# canonical Arch rendering of the upstream Debian-style version.
PKGVER="0.1.0.SNAPSHOT"
PKGREL="1"
ARCH="x86_64"
MAINTAINER="cloudBSD <admin@cloudbsd.org>"

ARTIFACT_NAME="${PROJECT}-${PKGVER}-${PKGREL}-${ARCH}.pkg.tar.zst"
ARTIFACT_PATH="/out/${ARTIFACT_NAME}"

# --- 0. Sanity-check the bind mounts ----------------------------
if [ ! -d /src ]; then
    echo "FATAL: /src is not a directory -- did you forget -v \$PWD:/src:ro ?" >&2
    exit 1
fi
if [ ! -d /out ]; then
    echo "FATAL: /out is not a directory -- did you forget -v \$PWD/../dist:/out:rw ?" >&2
    exit 1
fi
if [ ! -f /src/debian/DEBIAN/control ]; then
    echo "FATAL: /src/debian/DEBIAN/control missing -- is /src the honcho-inspector-ui repo root ?" >&2
    exit 1
fi

# --- 1. Stage the source tree ------------------------------------
STAGE="$(mktemp -d -t stage.XXXXXX)"
trap 'rm -rf "$STAGE"' EXIT

cp -a /src/. "$STAGE/"

mkdir -p "$STAGE/.pkg-scripts"
cp /src/debian/DEBIAN/postinst  "$STAGE/.pkg-scripts/postinst"
cp /src/debian/DEBIAN/prerm     "$STAGE/.pkg-scripts/prerm"
cp /src/debian/DEBIAN/postrm    "$STAGE/.pkg-scripts/postrm"
cp /src/debian/DEBIAN/changelog "$STAGE/.pkg-scripts/changelog"
chmod 0755 "$STAGE/.pkg-scripts/postinst" \
          "$STAGE/.pkg-scripts/prerm" \
          "$STAGE/.pkg-scripts/postrm"

# Strip the noise. Same list as the fpm-based builds. Note we keep
# packaging/ in place -- the package() function moves
# packaging/container/entrypoint.sh into /usr/local/bin/, and the
# few KB of remaining packaging/ tree ships harmlessly under
# /usr/local/share/honcho-inspector-ui/packaging/.
rm -rf "$STAGE/.angular" \
       "$STAGE/dist" \
       "$STAGE/.git" \
       "$STAGE/debian" \
       "$STAGE/node_modules"
find "$STAGE" -type f -name '*.log' -delete

# --- 2. Generate PKGBUILD + .INSTALL -----------------------------
# The PKGBUILD sits next to the staged source; makepkg's $srcdir
# defaults to the PKGBUILD's directory when source=() is empty,
# so $srcdir = $STAGE = the project root.
#
# The .INSTALL file uses pacman's bash-style function hooks. It
# creates the www-data user/group (Arch uses useradd/groupadd with
# -r instead of busybox's adduser --system), seeds /etc/honcho-
# inspector-ui/proxy.conf.json if absent, and reloads systemd +
# enables + restarts the unit. Removal mirrors: stop + disable.
PKGBUILD_FILE="$STAGE/PKGBUILD"
INSTALL_FILE="$STAGE/.INSTALL"

cat > "$PKGBUILD_FILE" <<'PKGBUILD_EOF'
# PKGBUILD for honcho-inspector-ui
# Generated by packaging/build/arch/entrypoint.sh. Do NOT edit by
# hand -- regenerate by running the build container.
pkgname=honcho-inspector-ui
pkgver=0.1.0.SNAPSHOT
pkgrel=1
pkgdesc="Honcho Inspector UI (Angular 22 dashboard). Runs ng serve as a node app under systemd, bound on 0.0.0.0:4200. node_modules is NOT shipped -- the systemd unit runs 'npm ci' as ExecStartPre on first boot so the right native binaries for the host arch get pulled automatically."
arch=('x86_64')
url="https://github.com/cloudbsdorg/honcho-inspector-ui"
license=('BSD3')
depends=('nodejs>=20' 'npm' 'shadow')
optdepends=('ca-certificates: TLS verification for HTTPS proxy targets')
provides=('honcho-inspector-ui')
conflicts=('honcho-inspector-ui')
backup=('etc/honcho-inspector-ui/proxy.conf.json')
install=.INSTALL

# No upstream tarball to fetch -- the build container bind-mounts
# the source tree into /src and stages it next to this PKGBUILD.
source=()
sha256sums=()

package() {
    # makepkg's $srcdir is $BUILDDIR/$pkgbase/src and is EMPTY when
    # source=() is empty (no upstream tarball to extract). The actual
    # staged source tree lives at $startdir (the directory containing
    # this PKGBUILD) -- in our case $STAGE. Use $startdir directly.
    local src="$startdir"

    # The full source tree (minus build artefacts we'll remove
    # below) lands at the canonical /usr/local/share/... path.
    # tar --exclude avoids recursing into .build (makepkg's
    # own BUILDDIR tree) and skips the PKGBUILD/.INSTALL/.pkg-scripts
    # scratch files. cpio -pdm silently produced "0 blocks" on
    # GNU cpio 2.15 here -- tar just works.
    install -d "$pkgdir/usr/local/share/honcho-inspector-ui"
    (cd "$src" && tar -cf - \
        --exclude=./.build \
        --exclude=./pkg \
        --exclude=./PKGBUILD \
        --exclude=./.INSTALL \
        --exclude=./.pkg-scripts \
        --exclude=./honcho-inspector-ui-*.pkg.tar.zst \
        . | tar -xf - -C "$pkgdir/usr/local/share/honcho-inspector-ui/")

    # Strip build artefacts that should not ship in the package.
    rm -f "$pkgdir/usr/local/share/honcho-inspector-ui/PKGBUILD"
    rm -f "$pkgdir/usr/local/share/honcho-inspector-ui/.INSTALL"
    rm -rf "$pkgdir/usr/local/share/honcho-inspector-ui/.pkg-scripts"

    # Move the systemd unit to its canonical path.
    if [ -f "$pkgdir/usr/local/share/honcho-inspector-ui/etc/systemd/honcho-inspector-ui.service" ]; then
        install -d "$pkgdir/etc/systemd/system"
        mv "$pkgdir/usr/local/share/honcho-inspector-ui/etc/systemd/honcho-inspector-ui.service" \
           "$pkgdir/etc/systemd/system/honcho-inspector-ui.service"
        rmdir "$pkgdir/usr/local/share/honcho-inspector-ui/etc/systemd" 2>/dev/null || true
    fi

    # Move the runtime entrypoint to /usr/local/bin (handy for
    # `sudo -u www-data /usr/local/bin/entrypoint.sh` debugging).
    if [ -f "$pkgdir/usr/local/share/honcho-inspector-ui/packaging/container/entrypoint.sh" ]; then
        install -d "$pkgdir/usr/local/bin"
        mv "$pkgdir/usr/local/share/honcho-inspector-ui/packaging/container/entrypoint.sh" \
           "$pkgdir/usr/local/bin/entrypoint.sh"
    fi

    # Move the proxy config template to /etc.
    if [ -f "$pkgdir/usr/local/share/honcho-inspector-ui/proxy.conf.json" ]; then
        install -d "$pkgdir/etc/honcho-inspector-ui"
        mv "$pkgdir/usr/local/share/honcho-inspector-ui/proxy.conf.json" \
           "$pkgdir/etc/honcho-inspector-ui/proxy.conf.json"
    fi

    # Move the changelog to the doc dir. The source path uses the
    # .pkg-scripts/ scratch dir we copied into at staging time.
    install -d "$pkgdir/usr/local/share/doc/honcho-inspector-ui"
    if [ -f "$src/.pkg-scripts/changelog" ]; then
        install -m644 "$src/.pkg-scripts/changelog" \
                    "$pkgdir/usr/local/share/doc/honcho-inspector-ui/changelog.Debian"
    fi
}
PKGBUILD_EOF

cat > "$INSTALL_FILE" <<'INSTALL_EOF'
#!/bin/bash
# .INSTALL -- pacman install script for honcho-inspector-ui.
# Generated by packaging/build/arch/entrypoint.sh. Functions follow
# the standard pacman(8) naming; pacman sources this file at the
# right point in the transaction and calls the matching function.

post_install() {
    # Service account. Arch uses useradd/groupadd with -r (system
    # account); the debian-flavored adduser in the upstream postinst
    # does not exist here. Idempotent: skip if already present.
    if ! getent group www-data >/dev/null; then
        groupadd -r www-data
    fi
    if ! getent passwd www-data >/dev/null; then
        useradd -r -d /var/lib/honcho-inspector-ui \
            -s /usr/sbin/nologin \
            -g www-data \
            -c "Honcho Inspector UI service account" \
            www-data
    fi

    # State, log, config dirs. 0770 lets www-data land the npm cache
    # under /var/lib on the first boot's ExecStartPre npm ci.
    install -d -m 0770 -o root -g www-data /var/lib/honcho-inspector-ui
    install -d -m 0770 -o root -g www-data /var/log/honcho-inspector-ui
    install -d -m 0750 -o root -g www-data /etc/honcho-inspector-ui

    chown -R root:www-data /usr/local/share/honcho-inspector-ui
    find /usr/local/share/honcho-inspector-ui -type d -exec chmod 0775 {} +

    # Seed proxy.conf.json only if missing (respect operator edits).
    if [ ! -f /etc/honcho-inspector-ui/proxy.conf.json ]; then
        install -m 0644 -o root -g www-data \
            /usr/local/share/honcho-inspector-ui/proxy.conf.json \
            /etc/honcho-inspector-ui/proxy.conf.json
    fi

    if [ -d /run/systemd/system ]; then
        systemctl daemon-reload
        systemctl enable honcho-inspector-ui.service
        systemctl restart honcho-inspector-ui.service || true
    fi
}

pre_remove() {
    if [ -d /run/systemd/system ]; then
        systemctl stop honcho-inspector-ui.service || true
        systemctl disable honcho-inspector-ui.service || true
    fi
}

post_remove() {
    true
}
INSTALL_EOF

chmod 0644 "$PKGBUILD_FILE"
chmod 0644 "$INSTALL_FILE"

# --- 3. Drop privileges and run makepkg --------------------------
# makepkg refuses to run as root. The orchestrator runs this
# container WITHOUT --userns=keep-id for arch specifically, so
# we ARE real root here. We chown the entire tree to `nobody`
# (the canonical non-root UID 65534 on Arch), then drop to it
# via setpriv --reuid=nobody --regid=nogroup. We use setpriv
# instead of runuser because runuser in some minimal containers
# refuses to switch to a nologin user. fakeroot wraps makepkg
# so the .pkg.tar.zst records root ownership.
if ! id nobody >/dev/null 2>&1; then
    useradd -M -d /nonexistent -s /usr/sbin/nologin nobody 2>/dev/null || true
fi
NOBODY_UID="$(id -u nobody)"
NOBODY_GID="$(id -g nobody)"
chmod 0755 "$STAGE"
chmod -R u+rwX,g+rX,o+rX "$STAGE"
chown -R "${NOBODY_UID}:${NOBODY_GID}" "$STAGE" 2>/dev/null || true

# BUILDDIR is intentionally NOT set -- makepkg then uses $STAGE as
# its startdir (the PKGBUILD's directory). package() refers to $startdir
# directly for the source tree because with source=() empty, makepkg's
# $srcdir is created but never populated.
setpriv --reuid="${NOBODY_UID}" --regid="${NOBODY_GID}" --clear-groups -- \
    env MAKEPKG_PACKAGER="${MAINTAINER}" \
        PKGDEST="${STAGE}" \
        SRCDEST="${STAGE}" \
    bash -c "cd '${STAGE}' && makepkg -s -f --noconfirm --skippgpcheck --nocolor"

# --- 4. Move the artifact to /out --------------------------------
# makepkg writes honcho-inspector-ui-0.1.0.SNAPSHOT-1-x86_64.pkg.tar.zst
# into the PKGBUILD's directory (= $STAGE). Glob for it in case the
# version string ever changes; we then move (not copy, the source
# path is in /tmp and gets trap'd anyway) the match to /out.
shopt -s nullglob 2>/dev/null || true
PKG_TARBALL=""
for f in "$STAGE"/honcho-inspector-ui-*.pkg.tar.zst; do
    PKG_TARBALL="$f"
    break
done
if [ -z "$PKG_TARBALL" ] || [ ! -f "$PKG_TARBALL" ]; then
    echo "FATAL: makepkg did not produce a .pkg.tar.zst in $STAGE" >&2
    exit 1
fi
cp "$PKG_TARBALL" "$ARTIFACT_PATH"

chown -R "${HOST_UID:-0}:${HOST_GID:-0}" /out

if [ ! -f "$ARTIFACT_PATH" ]; then
    echo "FATAL: copy succeeded but $ARTIFACT_PATH is missing" >&2
    exit 1
fi

echo "BUILT: $ARTIFACT_PATH"
exit 0
